package com.huatuo.customer.domain;

import java.io.Serializable;

@SuppressWarnings("serial")
public class DoctorTypePojo implements Serializable {
	
	/**
	 * 余号数
	 */
	private int num;
	
	/**
	 * 医生id
	 */
	private Long doctorId;
	
	/**
	 * 是否预约满(true已满,false没满)
	 */
	private boolean subscribeType;
	
	/**
	 * 是否开通预约
	 */
	private int isYy;
	
	/**
	 * 排队人数
	 */
	private int numLine;

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public Long getDoctorId() {
		return doctorId;
	}

	public void setDoctorId(Long doctorId) {
		this.doctorId = doctorId;
	}

	public boolean getSubscribeType() {
		return subscribeType;
	}

	public void setSubscribeType(boolean subscribeType) {
		this.subscribeType = subscribeType;
	}

	public int getIsYy() {
		return isYy;
	}

	public void setIsYy(int isYy) {
		this.isYy = isYy;
	}

	public int getNumLine() {
		return numLine;
	}

	public void setNumLine(int numLine) {
		this.numLine = numLine;
	}

}