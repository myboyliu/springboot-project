package com.huatuo.customer.domain;

import java.io.Serializable;

public class TeamOrderPojo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	/**
	 * 医生团队id
	 */
	private String dtTeamId;

	/**
	 * 医生团队名称
	 */
    private String teamName;
    
    /**
     * 服务时长
     */
    private int num;
    
	public String getDtTeamId() {
		return dtTeamId;
	}

	public void setDtTeamId(String dtTeamId) {
		this.dtTeamId = dtTeamId;
	}

	public String getTeamName() {
		return teamName;
	}

	public void setTeamName(String teamName) {
		this.teamName = teamName;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	@Override
	public String toString() {
		return "TeamOrderPojo [dtTeamId=" + dtTeamId + ", teamName=" + teamName
				+ ", num=" + num + "]";
	}

}
